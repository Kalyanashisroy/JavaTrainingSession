package JavaBasics;

public class DataTypes {
	public static void main(String[] args) {
		//Primitive data types: int,double,char,boolean
		//1. int:
		int i=10;
		    i=20;
		int j=30;
		System.out.println(j);
		int k=40;
		int l=-1;
		System.out.println(l);
		//2. double:
		double d=12.13;
		double d1=34.60;
		System.out.println(d1);
		double d2=100;//100.00
		//3. char: only single digit value should be written within single quote
		char c='a';
		char c1='A';
		char c2='$';
		char c3='1';
		//4. boolean:
		boolean b1=true;
		boolean b2=false;
		//5. string: is a class , not a data type
		String s="Hello World";
		String s1="Selenium";
		String s2="Hi this is my java code";
		String s3="1000";
		String s4="12.33";
		System.out.println(s2);
		
		
	}
}
